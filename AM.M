clc;
close all;
clear all;
vi=input('Enter Amplitude of the Messasge Signal ');
vc=input('Enter Amplitude of the carrier signal ');
fi=input('Enter Frequency of the Message Signal ');
fc=input('Enter frequency of the carrier signal ');
n=input('Enter the no of cycles');
wi=(2*3.14*fi);
wc=(2*3.14*fc);
subplot(3,1,1);
t=0:0.1:100;
n1=t/n;
s=(vi*sin(wi*t));
plot(n1,s);
subplot(3,1,2);
s1=(vc*sin(wc*t));
plot(n1,s1);
subplot(3,1,3);
m=vi/vc;
k=((m*vc)/2);
k1=cos((wi+wc)*t);
k2=cos((wi-wc)*t);
vam=(s1+k*(k2-k1));
plot(n1,vam);